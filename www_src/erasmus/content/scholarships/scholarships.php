<h3>General information</h3>
<p>While the Erasmus Mundus Programme ended in 2013, Action 1 master and doctoral consortia and Action 2 partnerships will continue to offer scholarships for the next academic year and beyond. From 2014 onwards, new master consortia that will provide scholarship opportunities are selected within the new Erasmus+ programme (2014-2020) for international higher education cooperation and mobility.</p>
<p>Check the following page for more information:</p>
<p><a href="http://eacea.ec.europa.eu/erasmus_mundus/funding/scholarships_students_academics_en.php">http://eacea.ec.europa.eu/erasmus_mundus/funding/scholarships_students_academics_en.php</a></p>
<hr>

<h3>Other Scholarships</h3>

<p>Europe offers a vast range of higher education courses, and scholarships from national, regional or other funds may also be available. You can visit the following website that brings together information on scholarships offered by national and regional authorities, as well as private sources of financing: <a href="http://www.scholarshipportal.eu">http://www.scholarshipportal.eu</a></p>
<hr>
<h3>How to apply</h3>
<p>Apply direct to the partnership via its website. If you are registered at one of the partner institutions, you should contact your International Office.</p>


